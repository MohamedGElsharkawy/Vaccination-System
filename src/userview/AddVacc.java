    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        if (!vaccname.getText().equals("")&&!vaccprice.getText().equals("") &&vaccdate.getText().contains("/") &&!vacctime.getText().equals("")){
            Product product = new Product();
            product.setVacc_Name(vaccname.getText());
            product.setDate(vaccdate.getText());
            
            try{
                   product.setTime(Integer.parseInt(vacctime.getText()));
                   product.setVacc_Price(Double.parseDouble(vaccprice.getText()));
                          if (product.check(vaccname.getText(),vaccdate.getText())){
               JOptionPane.showMessageDialog(null,"the deta exist plz change your inputs");  
                }
              else {
            if (product.addVacc()){
                JOptionPane.showMessageDialog(null,"the data added");
                resetPanelData();
            } else {
                JOptionPane.showMessageDialog(null,"the data did not add");
            }
        } 
            }catch(NumberFormatException e){
                  JOptionPane.showMessageDialog(null,"Price And Time Should be Numbers ONLY");
            }
        
        }else {
      JOptionPane.showMessageDialog(null,"Enter / with date");
     }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

