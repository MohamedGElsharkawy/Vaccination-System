    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
         if ( !vaccprice.getText().equals("") && !vaccdate.getText().equals("") && !vaccname.getText().equals("") && !vacctime.getText().equals("")){
                
              Product x = new Product();
              x.setVacc_Name(vaccname.getText());
              x.setDate(vaccdate.getText());
         try{
                       x.setVacc_Price(Integer.parseInt(vaccprice.getText()));
               x.setTime(Integer.parseInt(vacctime.getText()));
                 if (x.updateVacc(search.getText(),x)) {
               JOptionPane.showMessageDialog(null,"the data updated");
                resetPanelData();
            }else {
               JOptionPane.showMessageDialog(null,"This Product Doesn't Existed !");
            }
            
         }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null,"Price And Time Should Be Numbers ONLY");
         }
         }else{               
                    JOptionPane.showMessageDialog(null,"MISSING FIELDS....!");
                    }
         
    }//GEN-LAST:event_btnloginActionPerformed

