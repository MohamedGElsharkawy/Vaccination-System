    protected void resetPanelData() {
        vaccprice.setText("");
        vaccname.setText("");
        vaccdate.setText("");
        vacctime.setText("");
        search.setText("");

    }
    protected void setPanelData(Product pro) {
        vaccprice.setText(""+pro.getVacc_Price());
        vaccname.setText(""+pro.getVacc_Name());
         vaccdate.setText(""+pro.getDate());
        vacctime.setText(""+pro.getTime());

    }
