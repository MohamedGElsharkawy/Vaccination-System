    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
       if (!password.getText().equals("") && !email.getText().equals("") && !age.getText().equals("") && !username.getText().equals("")&&!phone_number.getText().equals("")){
        vaccination.customer cus = new vaccination.customer();
        cus.setname(username.getText());
        cus.setpassword(password.getText()); 
        cus.setemail(email.getText()); 
      try{
        cus.setage(Integer.parseInt(age.getText()));
        cus.setphone(Double.parseDouble(phone_number.getText()));
        
        if (cus.addcustomer()){
             JOptionPane.showMessageDialog(null,"the deta added");   
                resetPanelData();
            } else {
               JOptionPane.showMessageDialog(null,"the deta did not add"); 
            }
      }catch(NumberFormatException e){
           JOptionPane.showMessageDialog(null,"You Should Enter PhoneNumber And Age Numbers only"); 

      }
       }else{
          JOptionPane.showMessageDialog(null,"MISSING FIELDS...."); 

       }
       
       
    }//GEN-LAST:event_btnloginActionPerformed

