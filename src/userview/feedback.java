    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
if(jRadioButtonVeryBad.isSelected()||jRadioButtonBad.isSelected()||jRadioButtonNormal.isSelected()||jRadioButtonGood.isSelected()||jRadioButtonVeryGood.isSelected()){
    vaccination.feedback f=new vaccination.feedback();
    if(jRadioButtonVeryBad.isSelected())
    f.setVeryBad(true); 
    else
    f.setVeryBad(false);
    if(jRadioButtonBad.isSelected())
    f.setBad(true);
    else
            f.setBad(false);
    if(jRadioButtonNormal.isSelected())
    f.setNormal(true);
    else
            f.setNormal(false);
    if(jRadioButtonGood.isSelected())
    f.setGood(true);
    else
            f.setGood(false);
    if(jRadioButtonVeryGood.isSelected())
    f.setVeryGood(true);
    else
    f.setVeryGood(false);
        f.setMessage(jTextAreaMessage.getText());
    if(f.addRate()){
        jTextAreaMessage.setText("");
        jLabelRate.setText("Thank You");
    }
    else
        jLabelRate.setText("Fail to Insert ...!");
}
    else
    jLabelRate.setText("Please Choose Your Rate ...!");

    }//GEN-LAST:event_jButtonSubmitActionPerformed

