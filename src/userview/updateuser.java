    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        if (!username.getText().equals("") && !password.getText().equals("") && !email.getText().equals("") && !age.getText().equals("") && !phone_number.getText().equals("")){
        customer cus = new customer();
        cus.setname(username.getText());
        cus.setpassword(password.getText()); 
        cus.setemail(email.getText()); 
        
        try{
        cus.setage(Integer.parseInt(age.getText()));
        cus.setphone(Double.parseDouble(phone_number.getText()));
        
        if(cus.updatecustomer(Double.parseDouble(search.getText()),cus)){
        JOptionPane.showMessageDialog(null,"the data updated");
        resetPanelData();
        }else {
        JOptionPane.showMessageDialog(null,"check your inputs");
        }
        }catch(NumberFormatException e){
          JOptionPane.showMessageDialog(null,"You Should Enter PhoneNumber And Age Numbers only"); 

        }
        }else{
        JOptionPane.showMessageDialog(null,"complete the fields");
        }
    }//GEN-LAST:event_btndeleteActionPerformed

