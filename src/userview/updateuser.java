    protected void resetPanelData() {
        password.setText("");
        phone_number.setText("");
        username.setText("");
        email.setText("");
        age.setText("");
        search.setText("");
    }
    protected void setPanelData(customer cus) {
        password.setText(""+cus.getpassword());
        phone_number.setText(""+cus.getphone());
        username.setText(""+cus.getname());
        email.setText(""+cus.getemail());
        age.setText(""+cus.getage());
    }
