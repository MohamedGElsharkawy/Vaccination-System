    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try{ 
            if (!search.getText().equals("")) {
            customer x = new customer();
            customer returned = x.searchcustomer(Integer.parseInt(search.getText()));
            if (returned.getphone()> 0) {
                setPanelData(returned);
            } else {
                JOptionPane.showMessageDialog(null,"the data doesn't existed");
            }
        }else{
                JOptionPane.showMessageDialog(null,"Enter The Phone Number");}
        }catch(NumberFormatException e){
          JOptionPane.showMessageDialog(null,"You Should Enter Numbers ONLY !!");

        }
    }//GEN-LAST:event_jButton1ActionPerformed

